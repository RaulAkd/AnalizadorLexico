package clasesCompilador;

public class Lexema {
	
	String lexema;
	String TokenClass;
	
	public String getLexema() {
		return lexema;
	}
	public void setLexema(String lexema) {
		this.lexema = lexema;
	}
	public String getTokenClass() {
		return TokenClass;
	}
	public void setTokenClass(String tokenClass) {
		TokenClass = tokenClass;
	}
}
